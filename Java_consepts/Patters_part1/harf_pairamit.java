package Java_consepts.Patters_part1;

public class harf_pairamit {
    public static void main(String args[]) {
        for (int line = 1; line <= 5; line++) {
            for (int star = 1; star <= line; star++) {
                System.out.print(star);
            }
            System.out.println("");
        }
    }
}
