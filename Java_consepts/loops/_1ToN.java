package Java_consepts.loops;

import java.util.*;

public class _1ToN {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int count = 1;
        while (count <= n) {
            System.out.println("value of n : " + count);
            count++;
        }
    }

}
