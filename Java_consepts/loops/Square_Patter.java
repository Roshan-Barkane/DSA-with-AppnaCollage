package Java_consepts.loops;

public class Square_Patter {
    public static void main(String args[]) {
        // Print square patter with for loop
        for (int i = 0; i <= 3; i++) {
            for (int j = 0; j <= 3; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
